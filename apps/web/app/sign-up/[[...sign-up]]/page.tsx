export default function SignUpPage() {
  const [mounted, setMounted] = useState(false);
  const [clerkFailed, setClerkFailed] = useState(false);

  useEffect(() => {
    setMounted(true);
    // Set a timeout to detect if Clerk fails to load
    const timeout = setTimeout(() => {
      const clerkRoot = document.querySelector('[data-clerk-root]');
      if (!clerkRoot || clerkRoot.children.length === 0) {
        setClerkFailed(true);
      }
    }, 3000);

    return () => clearTimeout(timeout);
  }, []);

  // Always show fallback if Clerk isn't configured
  if (!isClerkConfigured || clerkFailed) {
    return <FallbackSignUp />;
  }

  // Show loading state while mounting
  if (!mounted) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gradient-to-b from-background to-muted/30">
        <Loader2 className="w-8 h-8 animate-spin text-primary" />
      </div>
    );
  }

  return (
    <div className="min-h-screen flex flex-col bg-gradient-to-b from-background to-muted/30">
      {/* Header */}
      <header className="p-4">
        <Link
          href="/"
          className="inline-flex items-center gap-2 text-sm text-muted-foreground hover:text-foreground"
        >
          <ArrowLeft className="w-4 h-4" />
          Back to Home
        </Link>
      </header>

      {/* Main Content */}
      <div className="flex-1 flex items-center justify-center" data-clerk-root>
        <SignUp
          appearance={{
            elements: {
              rootBox: "mx-auto",
              card: "shadow-xl",
            },
          }}
        />
      </div>
    </div>
  );
}
